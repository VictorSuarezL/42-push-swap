#include "libft.h"

t_list	*ft_lstnew(void *content)
{
	t_list	*n_list;

	n_list = (t_list *)malloc(sizeof(t_list));
	if (!n_list)
	{
		return (NULL);
	}
	n_list->content = content;
	n_list->next = NULL;
	return (n_list);
}
